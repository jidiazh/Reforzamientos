#  lista vacía
lista_vacia = []

#  lista con elementos
lista_con_elementos = [1, 8, 3,5]

# uso de if para confirmar si la lista esta vacia
if not lista_vacia:
    print("La lista vacía está efectivamente vacía.")
else:
    print("La lista vacía tiene elementos.")

# uso de if para confirmar si la lista no esta vacia
if not lista_con_elementos:
    print("La lista con elementos está vacía.")
else:
    print("La lista con elementos tiene elementos.")
